def _render_script() -> str:
    return """
document.querySelectorAll('.site-nav a').forEach((link) => {
  link.addEventListener('click', () => {
    document.body.classList.add('navigating');
    window.setTimeout(() => document.body.classList.remove('navigating'), 200);
  });
});
"""


def _render_export_readme(design: DesignSpec, copied_assets: list[str]) -> str:
    asset_lines = "\n".join(f"- {item}" for item in copied_assets) or "- No uploaded assets were bundled."
    return f"""# Generated Site Export

Project: {design.project_name}

## Included files

- Shared `styles.css`
- Shared `app.js`
- {len(design.pages)} HTML page(s)
- Uploaded assets copied into `assets/`
- `manifest.json`

## Before production launch

- Replace placeholder copy, proof points, and contact details
- Verify asset licenses for any external imagery you select
- Review mobile spacing and CTA destinations
- Add analytics, forms, or deployment-specific tags as needed

## Bundled assets

{asset_lines}
"""


def generate_static_export(
    project_id: str,
    design: DesignSpec,
    *,
    provider: str,
    model: str,
    asset_paths: list[str],
    image_plan: list[AssetReference],
) -> tuple[BuildManifest, str, str]:
    build_id = f"build_{uuid.uuid4().hex[:12]}"
    export_root = project_export_dir(project_id, build_id)
    copied_assets = copy_project_assets(asset_paths, export_root)

    generated_files: list[str] = []
    for page in design.pages:
        filename = make_index_filename(page.slug)
        file_path = export_root / filename
        file_path.write_text(_render_page(design, page.slug, copied_assets), encoding="utf-8")
        generated_files.append(filename)

    styles_path = export_root / "styles.css"
    styles_path.write_text(_render_styles(design), encoding="utf-8")
    generated_files.append("styles.css")

    script_path = export_root / "app.js"
    script_path.write_text(_render_script(), encoding="utf-8")
    generated_files.append("app.js")

    readme_path = export_root / "README.md"
    readme_path.write_text(_render_export_readme(design, copied_assets), encoding="utf-8")
    generated_files.append("README.md")

    manifest = BuildManifest(
        project_name=design.project_name,
        build_id=build_id,
        site_title=design.project_name,
        generated_at=datetime.now(UTC),
        provider=provider,  # type: ignore[arg-type]
        model=model,
        pages=[make_index_filename(page.slug) for page in design.pages],
        files=generated_files + copied_assets + ["manifest.json"],
        assets=image_plan,
        notes=[
            "Generated as a static brochure-style website export.",
            "Uploaded assets were bundled when present.",
            "Internet image recommendations remain metadata and are not downloaded automatically.",
        ],
    )

    manifest_path = export_root / "manifest.json"
    manifest_path.write_text(json.dumps(manifest.model_dump(mode="json"), indent=2), encoding="utf-8")

    zip_path = export_root.parent / f"{build_id}.zip"
    with zipfile.ZipFile(zip_path, "w", zipfile.ZIP_DEFLATED) as bundle:
        for path in export_root.rglob("*"):
            if path.is_file():
                bundle.write(path, path.relative_to(export_root))

    return manifest, str(export_root), str(zip_path)
