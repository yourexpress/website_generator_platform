def _render_nav(design: DesignSpec) -> str:
    links = []
    for page in design.pages:
        filename = make_index_filename(page.slug)
        label = page.title
        links.append(f'<a href="{filename}">{label}</a>')
    return "\n".join(links)


def _render_section(section_name: str, section_layout: str, page_title: str, visual_label: str) -> str:
    return f"""
    <section class="content-block">
      <div class="section-kicker">{page_title}</div>
      <h2>{section_name}</h2>
      <p>{section_layout.capitalize()} that keeps the story clean, direct, and conversion-minded.</p>
      <div class="section-grid">
        <div>
          <p>Use this block for tailored copy, proof points, and concise supporting detail.</p>
        </div>
        <div class="visual-placeholder">{visual_label}</div>
      </div>
    </section>
    """


def _render_page(design: DesignSpec, page_slug: str, copied_assets: list[str]) -> str:
    page = next(item for item in design.pages if item.slug == page_slug)
    first_asset = copied_assets[0] if copied_assets else ""
    hero_visual = (
        f'<img class="hero-image" src="{first_asset}" alt="{design.project_name} visual" />'
        if first_asset and page_slug == "index"
        else '<div class="hero-image placeholder">Add licensed or uploaded imagery here</div>'
    )
    sections_html = "\n".join(
        _render_section(section.name, section.layout, page.title, section.name)
        for section in page.sections
    )
    return f"""<!doctype html>
<html lang="en">
  <head>
    <meta charset="UTF-8" />
    <meta name="viewport" content="width=device-width, initial-scale=1.0" />
    <title>{page.title} | {design.project_name}</title>
    <link rel="stylesheet" href="styles.css" />
  </head>
  <body>
    <div class="background-glow glow-a"></div>
    <div class="background-glow glow-b"></div>
    <header class="site-header">
      <a class="brand" href="index.html">{design.project_name}</a>
      <nav class="site-nav">
        {_render_nav(design)}
      </nav>
    </header>
    <main>
      <section class="hero">
        <div class="hero-copy">
          <p class="eyebrow">Static export generated by Website Generator Platform</p>
          <h1>{page.hero_message}</h1>
          <p>{design.content_strategy[0]}</p>
          <div class="hero-actions">
            <a class="primary-button" href="#contact">Start a conversation</a>
            <a class="secondary-button" href="#proof">See the structure</a>
          </div>
        </div>
        <div class="hero-visual">
          {hero_visual}
        </div>
      </section>
      {sections_html}
      <section class="content-block contact-block" id="contact">
        <div class="section-kicker">Call to Action</div>
        <h2>Turn the draft into a launch-ready site</h2>
        <p>Replace this placeholder CTA with the project's real conversion action, booking link, or contact method.</p>
      </section>
    </main>
    <footer class="site-footer">
      <p>Generated for {design.project_name}. Replace placeholder copy, proof, and imagery before production launch.</p>
    </footer>
    <script src="app.js"></script>
  </body>
</html>
"""


